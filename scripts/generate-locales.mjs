const pluralRu = (one, few, many) => ({ one, few, many });
const pluralEn = (one, other) => ({ one, other });

function buildRu() {
  const p = {
    book: pluralRu('книга', 'книги', 'книг'),
    author: pluralRu('автор', 'автора', 'авторов'),
    series: pluralRu('серия', 'серии', 'серий'),
    genre: pluralRu('жанр', 'жанра', 'жанров'),
    language: pluralRu('язык', 'языка', 'языков'),
    record: pluralRu('запись', 'записи', 'записей'),
    archive: pluralRu('архив', 'архива', 'архивов'),
    user: pluralRu('пользователь', 'пользователя', 'пользователей'),
    admin: pluralRu('админ', 'админа', 'админов'),
    shelf: pluralRu('полка', 'полки', 'полок'),
    title: pluralRu('название', 'названия', 'названий'),
    source: pluralRu('источник', 'источника', 'источников'),
    bookmark: pluralRu('закладка', 'закладки', 'закладок')
  };
  const flat = {};
  for (const [type, forms] of Object.entries(p)) {
    flat[`plural.${type}.one`] = forms.one;
    flat[`plural.${type}.few`] = forms.few;
    flat[`plural.${type}.many`] = forms.many;
  }
  return flat;
}

function buildEn() {
  const p = {
    book: pluralEn('book', 'books'),
    author: pluralEn('author', 'authors'),
    series: pluralEn('series', 'series'),
    genre: pluralEn('genre', 'genres'),
    language: pluralEn('language', 'languages'),
    record: pluralEn('entry', 'entries'),
    archive: pluralEn('archive', 'archives'),
    user: pluralEn('user', 'users'),
    admin: pluralEn('admin', 'admins'),
    shelf: pluralEn('shelf', 'shelves'),
    title: pluralEn('title', 'titles'),
    source: pluralEn('source', 'sources'),
    bookmark: pluralEn('bookmark', 'bookmarks')
  };
  const flat = {};
  for (const [type, forms] of Object.entries(p)) {
    flat[`plural.${type}.one`] = forms.one;
    flat[`plural.${type}.other`] = forms.other;
  }
  return flat;
}

